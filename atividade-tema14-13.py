# Inicialização das variáveis
A = 10
B = 20

# Exibição dos valores iniciais
print("Valores iniciais: A = ",A," B = ",B)

# Troca dos valores utilizando uma variável temporária
temp = A
A = B
B = temp

# Exibição dos valores após a troca
print("Valores após a troca: A = ",A," B = ",B)
